class Program7{
	static void addition(int a , int b){
		int sum = a+b;
		System.out.println(a+" + "+b+" = "+ sum);
	}
	
	static void multiplication(int a , int b){
		int mul = a*b;
		System.out.println(a+" + "+b+" = "+ mul);
	}

	public static void main(String args[]){
		addition(10,12);
		multiplication(10,12);
	}

	
	
}
